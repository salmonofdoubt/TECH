package main

import "fmt"

func main() {
	m := make([]string, 2, 25)
	fmt.Println(m) // [ ]
	fmt.Println(&m[0], &m[1])

	change(m)
	fmt.Println(m)            // [Andre Baumann]
	fmt.Println(&m[0], &m[1]) // 0xc8200601a0 0xc8200601b0
}

func change(z []string) { //takes a string slice
	fmt.Println("passed in z :", z)
	z[0] = "Andre"
	z[1] = "Baumann"
}

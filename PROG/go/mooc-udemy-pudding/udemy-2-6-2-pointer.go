package main

import "fmt"

type mutable struct {
	a int
	b int
}

func (m mutable) StayTheSame() {
	m.a = 5
	m.b = 7
}

func (m *mutable) Mutate() {
	m.a = 5
	m.b = 7
}

func main() {
	m := &mutable{0, 0}
	fmt.Println(m)
	m.StayTheSame()
	fmt.Println(m)
	m.Mutate()
	fmt.Println(m)
}
